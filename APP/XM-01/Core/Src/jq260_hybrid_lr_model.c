static float sigmoidf_safe(float x)
{
    if (x >= 20.0f) return 1.0f;
    if (x <= -20.0f) return 0.0f;
    return 1.0f / (1.0f + expf(-x));
}

static float max_value_u8(const uint8_t *x)
{
    uint8_t mx = 0;
    for (uint16_t i = 0; i < JQ260_HYBRID_NUM_FEATURES; ++i) {
        if (x[i] > mx) mx = x[i];
    }
    return (mx == 0u) ? 1.0f : (float)mx;
}

static float lr_score_from_u8_norm(const uint8_t *x, const float *w, float b)
{
    const float mx = max_value_u8(x);
    float score = b;
    for (uint16_t i = 0; i < JQ260_HYBRID_NUM_FEATURES; ++i) {
        score += w[i] * ((float)x[i] / mx);
    }
    return score;
}

static float row_centroid(const uint8_t *x)
{
    float weighted = 0.0f;
    float total = 0.0f;
    for (uint8_t r = 0; r < JQ260_ROWS; ++r) {
        uint16_t row_sum = 0;
        for (uint8_t c = 0; c < JQ260_COLS; ++c) {
            row_sum += x[(uint16_t)r * JQ260_COLS + c];
        }
        weighted += (float)r * (float)row_sum;
        total += (float)row_sum;
    }
    return (total < 1.0e-6f) ? 13.0f : (weighted / total);
}

static int round_to_int(float x)
{
    return (x >= 0.0f) ? (int)(x + 0.5f) : (int)(x - 0.5f);
}

static void build_upper_shifted(const uint8_t *sensor_data, uint8_t *out)
{
    uint8_t upper[JQ260_HYBRID_NUM_FEATURES];
    memset(upper, 0, JQ260_HYBRID_NUM_FEATURES);
    memcpy(upper, sensor_data, JQ260_SPLIT_ROW * JQ260_COLS);

    memset(out, 0, JQ260_HYBRID_NUM_FEATURES);
    int dr = round_to_int(JQ260_TRAIN_CENTROID - row_centroid(upper));
    for (uint8_t r = 0; r < JQ260_ROWS; ++r) {
        int dst_r = (int)r + dr;
        if (dst_r < 0 || dst_r >= JQ260_ROWS) continue;
        memcpy(&out[(uint16_t)dst_r * JQ260_COLS],
               &upper[(uint16_t)r * JQ260_COLS],
               JQ260_COLS);
    }
}

static void build_lower_original_rows(const uint8_t *sensor_data, uint8_t *out)
{
    memset(out, 0, JQ260_HYBRID_NUM_FEATURES);
    memcpy(&out[JQ260_SPLIT_ROW * JQ260_COLS],
           &sensor_data[JQ260_SPLIT_ROW * JQ260_COLS],
           (JQ260_ROWS - JQ260_SPLIT_ROW) * JQ260_COLS);
}

uint8_t jq260_hybrid_person_count_predict(const uint8_t sensor_data[JQ260_HYBRID_NUM_FEATURES], float *confidence)
{
    float score = lr_score_from_u8_norm(sensor_data, s_person_count_w, s_person_count_b);
    float p_two = sigmoidf_safe(score);
    if (confidence != 0) {
        *confidence = (p_two >= 0.5f) ? p_two : (1.0f - p_two);
    }
    return (p_two >= 0.5f) ? JQ260_PERSON_COUNT_TWO : JQ260_PERSON_COUNT_ONE;
}

uint8_t jq260_hybrid_double_posture_predict(const uint8_t sensor_data[JQ260_HYBRID_NUM_FEATURES], uint8_t region, float *side_probability)
{
    uint8_t part[JQ260_HYBRID_NUM_FEATURES];
    if (region == JQ260_REGION_UPPER) {
        build_upper_shifted(sensor_data, part);
    } else {
        build_lower_original_rows(sensor_data, part);
    }

    float score = lr_score_from_u8_norm(part, s_double_posture_w, s_double_posture_b);
    float p_side = sigmoidf_safe(score);
    if (side_probability != 0) {
        *side_probability = p_side;
    }
    return (p_side >= 0.5f) ? JQ260_POSTURE_SIDE : JQ260_POSTURE_SUPINE;
}
